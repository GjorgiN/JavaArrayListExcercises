package arrayListExcercises;

import java.util.ArrayList;

public class Excercise1 {
	
	// add elements in array list
	
	public static void main(String[] args) {
		
		ArrayList<String> colors = new ArrayList<>();
		
		System.out.println(colors);
		
		colors.add("red");
		System.out.println(colors);
		
		colors.add("Green");
		System.out.println(colors);

		colors.add("Orange");
		System.out.println(colors);
		
		colors.add("White");
		System.out.println(colors);
		
		colors.add("Black");
		System.out.println(colors);
		
	}
	
}
